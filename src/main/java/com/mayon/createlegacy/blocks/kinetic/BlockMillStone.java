package com.mayon.createlegacy.blocks.kinetic;

import com.mayon.createlegacy.CreateLegacy;
import com.mayon.createlegacy.mainRegistry.ModBlocks;
import com.mayon.createlegacy.mainRegistry.ModItems;
import com.mayon.createlegacy.tileentity.TileEntityMillStone;
import com.mayon.createlegacy.util.IHasModel;
import com.mayon.createlegacy.util.IHasRotation;
import net.minecraft.block.Block;
import net.minecraft.block.ITileEntityProvider;
import net.minecraft.block.SoundType;
import net.minecraft.block.material.MapColor;
import net.minecraft.block.material.Material;
import net.minecraft.block.state.IBlockState;
import net.minecraft.entity.item.EntityItem;
import net.minecraft.entity.player.EntityPlayer;
import net.minecraft.item.Item;
import net.minecraft.item.ItemBlock;
import net.minecraft.tileentity.TileEntity;
import net.minecraft.util.EnumFacing;
import net.minecraft.util.EnumHand;
import net.minecraft.util.math.BlockPos;
import net.minecraft.world.World;

import javax.annotation.Nullable;

public class BlockMillStone extends Block implements ITileEntityProvider, IHasModel, IHasRotation {
    public BlockMillStone() {
        super(Material.ROCK);
        this.translucent = true;
        this.fullBlock = false;
        setLightOpacity(0);

        setUnlocalizedName("create:millstone");
        setRegistryName("millstone");
        setCreativeTab(CreateLegacy.TAB_CREATE);
        setHarvestLevel("pickaxe", 0);
        setHardness(1);
        setResistance(2);

        setDefaultState(this.blockState.getBaseState());

        ModBlocks.BLOCKS.add(this);
        ModItems.ITEMS.add(new ItemBlock(this).setRegistryName(this.getRegistryName()));


    }

    @Override
    public void registerModels() {
        CreateLegacy.proxy.registerItemRenderer(Item.getItemFromBlock(this), 0, "inventory");
    }

    @Nullable
    @Override
    public TileEntity createNewTileEntity(World worldIn, int meta) {
        return new TileEntityMillStone();
    }

    @Override
    public boolean onBlockActivated(World worldIn, BlockPos pos, IBlockState state, EntityPlayer playerIn, EnumHand hand, EnumFacing facing, float hitX, float hitY, float hitZ) {
        TileEntityMillStone tileEntityMillStone = (TileEntityMillStone) worldIn.getTileEntity(pos);

        if (tileEntityMillStone == null) return false;
        if (!worldIn.isRemote) tileEntityMillStone.removeItemStacksToPlayer(worldIn, playerIn);

        return true;
    }

    @Override
    public boolean isFullBlock(IBlockState state) {
        return false;
    }

    @Override
    public boolean isFullCube(IBlockState state) {
        return false;
    }

    @Override
    public boolean isTranslucent(IBlockState state) {
        return true;
    }

    @Override
    public boolean isOpaqueCube(IBlockState state) {
        return false;
    }

    @Override
    public void onBlockHarvested(World worldIn, BlockPos pos, IBlockState state, EntityPlayer player) {
        TileEntityMillStone tileEntity = (TileEntityMillStone) worldIn.getTileEntity(pos);

        if (tileEntity != null) tileEntity.dropItems();
    }
}
